package br.com.ventisol.sankhya.model.vo;

public class SankhyaEspecificacao {
    private Integer numPro;
    private Integer numEspe;
    private String nome;
    private String valor;
    private String tipo;

    public Integer getNumPro() {
        return numPro;
    }

    public void setNumPro(Integer numPro) {
        this.numPro = numPro;
    }

    public Integer getNumEspe() {
        return numEspe;
    }

    public void setNumEspe(Integer numEspe) {
        this.numEspe = numEspe;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
}
